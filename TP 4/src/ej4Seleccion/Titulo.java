package ej4Seleccion;

public class Titulo {

	private String nombre;
	private int anio;
	
	public Titulo (String nombre, int anio) {
		this.nombre = nombre;
		this.anio = anio;
	}

	
	
	
	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getAnio() {
		return anio;
	}

	public void setAnio(int anio) {
		this.anio = anio;
	}
	
	
	
}
